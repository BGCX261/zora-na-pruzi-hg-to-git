

def velikost(hodnota,  jednotka):
    return {'hodnota': hodnota,  'jednotka': jednotka}

def px(hodnota):
    return velikost(hodnota,  'px')
    
def em(hodnota):
    return velikost(hodnota,  'em')
    
    
def mm(hodnota):
    return velikost(hodnota,  'mm')
    
def cm(hodnota):
    return velikost(hodnota,  'cm')
